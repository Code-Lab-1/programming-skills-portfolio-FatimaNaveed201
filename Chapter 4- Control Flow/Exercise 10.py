#Check wether the given alphabet is a vowel or not

letter = input("Enter an alphabet : ")
if letter == "a" or letter == "e" or letter == "i" or letter == "o" or letter == "u":
    print("The given alphabet is a vowel.")
elif letter == "A" or letter == "E" or letter == "I" or letter == "O" or letter == "U":
    print("The given alphabet is a vowel.")
else: 
    print("The given alphabet is not a vowel.")