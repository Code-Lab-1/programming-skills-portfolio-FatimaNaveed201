# Exercise 7: Seeing the World

places = ["Mauritius", "Bora Bora", "Paris", "London", "Turkey"]

print ("The Original Order: ")
print (places)

print ("\nAlphabetical Order: ")
print(sorted(places))

print ("\nThe Original Order: ")
print(places)

print("\nReversed Alphabetical Order: ")
print(sorted(places, reverse=True))

print ("\nThe Original Order: ")
print(places)

print("\nReversed: ")
places.reverse()
print(places)

print("\nThe Original Order: ")
places.reverse()
print(places)

print ("\nAlphabetical: ")
places.sort()
print(places)

print("\nReversed Alphabetical Order: ")
places.sort(reverse=True)
print(places)
